// ── Helpers ──────────────────────────────────────────────────────────────────

function msg(content: string): [{ role: 'user'; content: string }] {
  return [{ role: 'user' as const, content }];
}

function withSystem(
  system: string,
  user: string,
): Array<{ role: 'user' | 'system'; content: string }> {
  return [
    { role: 'system' as const, content: system },
    { role: 'user' as const, content: user },
  ];
}

