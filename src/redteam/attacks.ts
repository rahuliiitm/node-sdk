/**
 * Inject the user's system prompt into prompt_leakage attack payloads
 * that need a real system prompt to test against.
 */
export function injectSystemPrompt(
  attacks: AttackPayload[],
  systemPrompt: string,
): AttackPayload[] {
  return attacks.map((a) => {
    if (a.category !== 'prompt_leakage') return a;
    // Add system prompt so leakage detection has something to compare against
    const hasSystem = a.messages.some((m) => m.role === 'system');
    if (hasSystem) return a;
    return {
      ...a,
      messages: [{ role: 'system' as const, content: systemPrompt }, ...a.messages],
    };
  });
}

/**
 * Get built-in attacks, optionally filtered by category.
 */
export function getBuiltInAttacks(categories?: readonly string[]): AttackPayload[] {
  if (!categories || categories.length === 0) return [...BUILT_IN_ATTACKS];
  const catSet = new Set(categories);
  return BUILT_IN_ATTACKS.filter((a) => catSet.has(a.category));
}
